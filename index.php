        <h1>Estadistica inferencial</h1>
        <p>La estadística inferencial es una parte de la estadística que comprende los métodos y procedimientos que por medio de la inducción determina propiedades de una población estadística, a partir de una parte de esta. Su objetivo es obtener conclusiones útiles para hacer razonamientos deductivos sobre una totalidad, basándose en la información numérica dada por la muestra.</p>
        <p>En inferencia estadística se llama estimación al conjunto de técnicas que permiten dar un valor aproximado de un parámetro de una población a partir de los datos proporcionados por una muestra. La estimación se divide en tres grandes bloques, cada uno de los cuales tiene distintos métodos que se usan en función de las características y propósitos del estudio:</p>
        <ul>
            <li>Estimacion Puntual</li>
            <li>Estimacion Por intervalos</li>
            <li>Estimacion Bayeciana</li>
